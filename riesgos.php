                        <i class="fa fa-check text-primary mr-3"></i> Un ataque de hombre en el medio (MiTM) ocurre cuando 
                        un ciberdelincuente se apodera de un dispositivo sin que el dueño lo sepa y utiliza esa conexión para espiar
                        o robar información importante. Es como si alguien invisible se metiera entre tú y una página web a la que 
                        estás accediendo, viendo todo lo que haces y copiando lo que envías, como contraseñas o datos bancarios. 
                        El atacante puede interceptar la información antes de enviarla al lugar correcto, lo que le permite obtener 
                        detalles confidenciales, como tus cuentas o tarjetas de crédito. Hay muchos tipos de programas maliciosos 
                        (malware) que pueden realizar ataques MiTM. Estos programas pueden instalarse sin que te des cuenta al 
                        descargar algo peligroso o usar redes no seguras. Una vez que el malware está en el dispositivo, 
                        el atacante puede espiar tus datos sin que lo notes, por lo que es importante usar conexiones seguras y 
                        tener siempre el software actualizado.
                    </li>
