                <h1 class="mb-4"> BOTNET </h1>
                <p> Una botnet es un grupo de computadoras infectadas llamadas bots que son controladas por una persona mala (hacker) 
                    sin que los dueños lo sepan. Los bots se crean cuando una computadora se infecta, por ejemplo, al abrir un archivo 
                    o visitar un sitio web peligroso. Una vez infectada, la computadora se convierte en parte de una red de bots, 
                    llamada **botnet**, que puede tener miles o incluso millones de computadoras conectadas entre sí a través de 
                    Internet. Un hacker puede controlar esta red usando un servidor especial llamado **servidor de comando y control. 
                    Las botnets pueden ser usadas para hacer cosas malas, como enviar virus, realizar ataques DDoS 
                    (para bloquear sitios web), mandar correos basura (spam) o intentar adivinar contraseñas. 
                    A veces, los hackers alquilan estas botnets a otras personas para que hagan cosas ilegales. 
                    Empresas como Cisco trabajan para bloquear estas botnets usando filtros especiales que identifican el tráfico 
                    sospechoso y evitan que se propaguen.
                </p>
