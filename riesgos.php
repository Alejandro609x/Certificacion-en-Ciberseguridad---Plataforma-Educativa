                <h1 class="mb-4"> Ataques en el camino </h1>
                <p> Un ataque en el camino: Es cuando una persona mala se mete en medio de la comunicación entre dos dispositivos, 
                    como tu computadora y una página web, sin que lo sepas. Imagina que estás enviando una carta a un amigo, pero 
                    alguien en el camino intercepta esa carta, la lee y luego decide cambiar lo que escribiste antes de que llegue 
                    a tu amigo. En el mundo digital, un atacante puede hacer lo mismo: se coloca entre tu dispositivo y el servidor 
                    al que te conectas para robar información, como contraseñas o datos personales, o cambiar lo que ves o envías. 
                    Este tipo de ataque puede ocurrir en conexiones no seguras, como redes Wi-Fi públicas, y los hackers pueden hacerse 
                    pasar por el servidor o por ti para engañar a ambas partes y robar información importante.
                </p>
            </div>
